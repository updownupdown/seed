<h1>Users</h1>

<p class="bold red">This feature is for advanced users only!</p>

<p>Accessing the WordPress back-end (admin) requires a user login and password. Users are managed in the <strong>Users</strong> tab. If you do not see the Users tab, it means you do not have the required user permission to do so; ask your developer to upgrade your user role (usually from <em>Editor</em> to <em>Admin</em>).</p>

<hr />

<h2>User Role &amp; Permissions</h2>
<p>Different roles can be assigned to each user. Each role comes with a set of permissions that activate certain areas of the back-end (and sometimes the front-end). Ideally, users are given access to what they need to control, and nothing more, otherwise users that are only supposed to write blog posts or edit pages are able to change the website settings or other important areas of the back-end.</p>

<p>Here is a summary of the different roles:</p>
<ul>
	<li><strong>Administrator</strong> - somebody who has access to all the administration features within a single site; an 'admin' can change all the settings, install and delete plugins, etc.</li>
	<li><strong>Editor</strong> - somebody who can publish and manage posts including the posts of other users (this is typically the access that is given to clients to manage their website, unless they request access to core settings and know what they are doing).</li>
	<li><strong>Author</strong> - somebody who can publish and manage their own posts.</li>
	<li><strong>Contributor</strong> - somebody who can write and manage their own posts but cannot publish them.</li>
	<li><strong>Subscriber</strong> - somebody who can only manage their profile (this is usually the role of members of the public who sign-up to the website to post comments, when that functionality is made available).</li>
</ul>

<hr />

<h2>Adding Users</h2>
<p>You can easily add new users <strong>if you have an administrator user role</strong> (editors cannot add users, they can only edit their own profile, change their password, etc.).</p>

<p>Under the 'Users' tab, click 'Add New'. Enter a username (recommended format is 'bobsmith' or 'bob.smith', without capital letters, spaces or other symbols). Enter an email, first and last name, and a strong password. Secure passwords are <strong>highly recommended</strong> (if not straight out required): use a mix of lowercase and uppercase letters, numbers and special characters (e.g. 'lOrEmIpsUm45#%'), or better yet, a completely random set of characters (e.g. 'djt*r943#(FD79#%B').</p>

<p>Select a role for the new user. The 'Editor' role is recommended if you want someone to be able to edit posts and pages; 'Administrator' if you want the person to access ALL the website settings; user 'Subscriber' if you don't want the user to be able to edit anything other than their own profile.</p>

<p>You can choose to send the new user their password by email (to the email you specified above) for their own records, which is especially useful if you created a strong password for them.</p>

<p>Click 'Add New User' to create the user.</p>

<hr />

<h2>Editing, Promoting, &amp; Deleting Users</h2>
<p>From the users list, you can click on their username to edit their profile, change their password, or change their role. You can also hover over the username and click the 'Delete' link to delete the user.</p>