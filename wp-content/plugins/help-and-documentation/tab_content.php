<h3>Visual tab &amp; Text Tab</h3>
<p>You can switch between the Visual tab and Text tab at the top right of the editor to view the content formatting as it will appear on the front-end (Visual), or view the HTML code directly (Text). The Text view is for advanced users only; it can be useful to add CSS classes, view and remove empty paragraphs, or clean up the formatting.</p>

<h3>Pasting Content from Word or Other Sources</h3>
<p>When copying text from other sources, some hidden formatting (code) can be copied as well, even though it may not be visible when pasted in the content editor. To avoid problems in the way text appears when pasting text from a rich text editor, use the <strong>Paste as plain text</strong> icon (notepad with a T). Press the button (a dialog will appear the first time you press it); this will activate the "Paste as plain text" mode which will strip away any formatting from the text you are pasting.</p>

<hr />

<h2>Paragraph &amp; Headings, Lists, Basic Formatting</h2>
<p>You can easily change the formatting of text in the content editor. You can use default 'Paragraph' text, one of six headings (Heading One through Heading Six, from largest to smallest), as well as lists and blockquotes.</p>

<p>By default, all text is displayed as 'Paragraph' text. You can use one of six <strong>headings</strong> by selecting a line of text and selecting a heading in the format menu (on the left of the second line of icons).</p>

<p>You can format text and make it bold, italic, strikethrough, or underlined by using the appropriate icons. You can make unordered (bulleted) lists as well as ordered (numbered) lists. You can change the text alignment from left aligned to center or right aligned, or make it justified, as well as adding or removing indents.</p>

<p><strong>If you are having difficulty in applying formatting to a selection, use the eraser icon to clear the formatting on that selection.</strong></p>

<hr />

<h2>Inserting Links</h2>
<ol>
	<li>Select one or multiple words and click the <strong>Insert/Edit Link</strong> 'chain' icon.</li>
	<li>Enter a URL in the URL field and a title in the Title field (optional).</li>
	<li>For external links (outside your website), it's recommended to open the link in a new window/tab (see relevant checkbox).</li>
	<li>You can link to posts and pages on your website by clicking on <strong>Or link to existing content</strong> and selecting (or searching) for content.</li>
</ol>

<hr />

<h2>Inserting Email Links</h2>
<p>To add an email link, use the same process as when inserting a regular link, but enter <strong>mailto:ENTER_EMAIL_HERE</strong> into the URL field (e.g. <strong>mailto:john@example.com</strong>).</p>

<hr />

<h2>Formats</h2>
<p>You can use the "Formats" drop down menu to toggle special styles on links or images. Certain styles are only available when the right element is selected, like an image or a link.</p>