<h1>Content Editor</h1>

<p>The content editor in WordPress is used to edit the content of posts, pages, and custom post types (like testimonials, products and more). Make sure the <strong>Kitchen Sink (second row of icons)</strong> is activated so you have access to more formatting options in the second row of icons; the Kitchen Sink button is circled in red in the image below.</p>

