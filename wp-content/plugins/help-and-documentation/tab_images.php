<h1>Media Library</h1>
<p>All the images you upload in posts, pages or custom post types (slides, testimonials, etc.) are stored on the server, and have an entry in the database. You can manage all your file uploads in the <strong>Media Library</strong> tab on the left.</p>

<hr />

<h2>Uploading Images</h2>
<p>You can upload images in batches through the Media Library and later place them in various pages or posts. If you do so, the images will not be <em>attached</em> to any page/post. You can also upload images while editing a page/post, in which case the image will be attached to that page/post. This is useful, since the Media Library lists which page/post every image is attached to (has been uploaded to), or if the image is <em>unattached</em>. You could then delete images that are no longer used by any page/post to clean up the Media Library and make room on your server.</p>

<p>To add images through the Media Library, click the <strong>Media</strong> tab on the left, then click 'Add New'. You can drag-and-drop files from your computer's file explorer, or click the <em>"Select Files"</em> button to use the file browser dialogue.</p>

<hr />

<h2>Adding Images to Pages/Posts</h2>
<p>It is usually more convenient to upload images while editing a page/post. When editing content in the content editor, place your cursor where you want the image to appear and <strong>click the 'Add Media'</strong> button above the content editor.</p>

<p>You can upload a new image or select one from the Media Library. From the <strong>Upload Files</strong> section of the pop-up dialog, you can drag-and-drop files from your computer's file explorer, or click the <em>"Select Files"</em> button to use the file browser dialogue. You can upload multiple files at the same time.</p>

<p>After the image has finished uploading, <strong>you should edit the image details and select the display settings before inserting it into the page/post</strong> (see below for more details). Once that is done, click <span class="blue bold">Insert into page/post</span> to insert the image where you specified earlier (cursor location).</p>

<hr />

<h2>Image Details &amp; Display Settings</h2>

<h3>Title, Caption, Alt Text and Description</h3>
<p>The image <strong>Title</strong> and <strong>Alt Text</strong> should be a short description or series of keywords that identify your image. You can edit those tags on the right side in the Media popup. These tags have three main functions:</p>
<ul>
	<li>They are used to search for images in the Media Library.</li>
	<li>The Title and Alt 'tags' are placed in the image embed code. Those tags are used by search engines to recognize what the image is about.</li>
	<li>The Title shows up as a small tooltip when visitors hover over your image.</li>
</ul>

<p>The image <strong>Caption</strong> is used to add a text caption underneath the image (looks like a caption below a framed picture). Editing the image properties and adding or removing text in the 'caption' field will automatically add or remove a caption below the image.</p>

<p>The <strong>Description</strong> is sometimes used in image galleries.</p>

<p>All those tags are optional, but <em>it is highly recommended to tag the image with a Title and Alt Text!</em></p>

<h3>Alignment, Linking &amp; Size</h3>
<p class="bold">Before inserting the image in the content editor, make sure you select proper alignment, link and size settings:</p>

<p>The <strong>Alignment</strong> can be set to (None/Left/Center/Right).</p>

<p>The image <strong>Link</strong> can be set to:</p>
<ul>
	<li><em>Media File</em>: in the case of an image, this will link to the original, full size version of the image</li>
	<li><em>Attachment Page</em>: a near-empty page with the image in the content area</li>
	<li><em>Custom URL</em>: if you want the image to link to another page or website</li>
	<li><em>None</em>: <strong>recommended</strong>; the image simply shows up as an image and doesn't link to anything when clicked.</li>
</ul>

<p>The image <strong>Size</strong> can be set to <em>Thumbnail, Medium, Large, or Full Size</em>. The dimensions are shown in the select menu, and vary depending on the website settings and the dimensions of the image uploaded.</p>

<hr />

<h2>Formats (Shadows and Borders)</h2>
<p>You can use the "Formats" drop down menu to toggle special styles on images. Select the image by clicking on it once, then use "Formats -> Toggle style..." to apply a shadow or a border to the image. You can also select that option again to toggle the shadow or border off.</p>

<hr />

<h2>Editing &amp; Deleting Embedded Images</h2>
<p>Once added to the post, you can click on the image; two icons will appear on the top left of the image. Click on the 'landscape' icon to <strong>edit the image</strong>. From the dialogue, you can scale down the image, change its alignment, or edit the Title, Alt Text, Caption, and link. You can also click the red icon to <strong>delete the image</strong> from the content area (the image will still remain in the Media Library).</p>