<h1>Pages</h1>
<p>This section assumes you are familiar with the WordPress basics section of this documentation.</p>
<p>To manage pages, start by clicking on the <strong>"Pages" tab</strong>.</p>

<a href="http://codex.wordpress.org/Pages" class="button" target="_blank">Official documentation on Pages</a>

<hr />

<h2>Previewing, Saving and Updating Pages</h2>
<p>You can <strong>preview your page before you save it</strong> to see what your changes look like using the "Preview Changes" button in the "Publish" box.</p>

<p>You can <strong>Publish or Save your page</strong> and make it 'live' but clicking on the "Publish" button in the "Publish" box. Once your page has been published, the button will now say "Save". Note that once you publish a page, it will not appear in the menu yet. Read the "Menus" section of this documentation to learn how to add pages to the menu.</p>

<p>You can <strong>Unpublish your page</strong> by changing its visibility from 'Public' to 'Draft' under the "Publish" box. A link might still exist for that page if the page is still in the menu.</p>

<p>You can <strong>Delete your page</strong> by clicking the 'Move to Trash' button in the "Publish" box.</p>

<hr />

<h2>Editing Pages</h2>

<h3>Title &amp; Permalink</h3>
<p>After adding a new page, write a Title for it at the top of the page. Once you save your page, WordPress will create a 'permalink' for that page, which is just a clean URL. You can edit the permalink, but beware of creating conflicts with other pages with the same name. Permalinks should only use lowercase letters, numbers, and hyphens.</p>

<h3>Content</h3>
<p>The content of the page goes in the "content editor", the large text area with editing buttons (bold, italic, etc.) as well as the "Add Media" button.</em></p>

<h3>Custom Fields</h3>
<p>Many pages will be setup with "custom fields", like text fields, gallery fields, or a flexible content field. Follow the instructions below each field.</p>

<h3>Page Revisions</h3>
<p>Every time you publish or update a page, a new revision is created in the database. If you ever want to go back to a previous version of a page, click the <strong>Browse</strong> link next to 'Revisions' in the "Publish" box. You can select the revision you want and restore it, then update the page.</p>

<h3>Page Attributes</h3>
<p>The "Page Attributes" box allows you to select a page Parent (optional) and a page template. If in doubt, do not change these settings.</p>

<p>All pages use a <strong>Page Template</strong>, which has been selected by the developer (unless it was later changed, or a new page was created, in which case the default page template is selected). Each page template has a coded file in the website theme which lays out a page in a particular way, usually with its own sidebars, graphics, scripts or other code which cannot be edited in a simple content editor.</p>

<p>In most cases, <strong>you do not have to worry about page templates, and <span class="red">it is recommended not to change page templates on any of the pages</span></strong> unless you know what you're doing. However, it is useful to know what page templates are, so you understand why some pages have a particular layout, extra editable fields, or display content in a different way.</p>