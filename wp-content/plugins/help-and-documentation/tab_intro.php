<p>Different post types will have different columns; pages may not have thumbnails, but bios might have them. Testimonials might have a testimonials excerpt column, etc. You can toggle columns on or off by using the "Screen Options" button at the top right. When you click on a single post, it will also has its own set of fields (title, content, date, featured image, etc.).</p>

<p>Also note the several ways in which you can search posts: filtering by date, by category, using the search box, or using the pagination for longer lists of posts.</p>

<h4>Adding, Editing, Deleting and Restoring Posts</h4>
<p>This functionality is similar for most post types.</p>

<ul>
	<li><strong>To add a post</strong>, find the "Add New" button at the top of the page or in the sidebar. Depending on the post type, it might say "Add New Page", "Add New Slide", etc.</li>
	<li><strong>To edit a post</strong>, click on its title, or hover it and click on the 'Edit' link.</li>
	<li><strong>To delete a post</strong>, hover on its title and click the 'Trash' link.</li>
	<li><strong>To restore a post</strong>, click on the 'Trash' link at the top of the page, find the post in the Trash, hover on its title and click the 'Restore' button. It will be returned to the main post list.</li>
	<li><strong>To delete a post permanently</strong>, go to the Trash again, but click on 'Delete permanently' instead. You can also empty the trash using the "Empty Trash" button.</li>
</ul>