<h1>WordPress Basics</h1>

<h2>WordPress Dashboard</h2>

<p>Once you are logged in, the first thing you will see is the Dashboard. It shows you some statistics and information relevant to your website. Note the following points of interest:</p>

<ul>
	<li><strong>User bar (top):</strong> The user bar has a link to the front-end of your website (top left), and a link to edit your profile as well as logout (top right). You generally do not need to use the user bar.</li>
	<li><strong>Sidebar (left):</strong> The sidebar lists links or tabs for each section of the back-end. The tabs shown will depend on your user permissions and your website's features.</li>	
	<li><strong>Screen Options (near top right):</strong> The 'Screen Options' button is shown on most pages throughout the back-end. It's a very handy way of showing/hiding certain boxes or columns depending on your preferences. For example, on the Dashboard you may want to hide the WordPress News and the Quick Draft boxes. When showing a list of Pages, you may want to remove the Author or Date column because they are not relevant.</li>
</ul>

