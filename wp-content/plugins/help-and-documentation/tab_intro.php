<h2>Managing Posts</h2>
<p>Virtually everything you're going to be doing in WordPress will involve different types of "posts": blog posts, pages (a special kind of posts), and "custom post types" (CPTs). CPTs may include staff bios, testimonials, resources, etc.</p>

<p>All post types are managed roughly the same way, so once you learn to edit a page, you will find it easy to edit a blog post, or a resource, etc. Each post type usually has its own tab in the sidebar. When you click on that tab, you will see a list of all the posts for the selected type. For blog posts, the list will look something like this:</p>

<div class="caption">
<img src="<?php echoImg('basics_lists.jpg'); ?>" width="600" height="316"/>
